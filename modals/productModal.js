const mongoose = require("mongoose");

const productSchema = mongoose.Schema(
  {
    product_id: {
      type: String,
      required: true,
      trim: true,
      unique: true,
    },
    title: {
      type: String,
      required: true,
      trim: true,
    },
    price: {
      type: Number,
      trim: true,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    images: {
      type: Object,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    checked: {
      type: Boolean,
      default: 0,
    },
  },
  {
    timestamp: true,
  }
);
module.exports = mongoose.model("product", productSchema);
